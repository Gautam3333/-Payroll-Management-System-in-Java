    // searching for a record
    private void getRecord()
    {
        try
        {
            String selectQuery = "select * from paydb.staff_info where emp_id = ? ";
            PreparedStatement ps = conn.prepareStatement(selectQuery);
            ps.setString(1, txt_search.getText());
            ResultSet rs = ps.executeQuery();
            rs.first();
            
            txt_empid.setText(rs.getString(1));
            txt_empid.setEditable(false);
            txt_firstname.setText(rs.getString(2));
            txt_surname.setText(rs.getString(3));
            String gen = rs.getString(4);
            if (gen.equals("Male"))
                r_male.setSelected(true);
            else
                r_female.setSelected(true);
            String dob = rs.getString(5);
            ((JTextField)DC_dob.getDateEditor().getUiComponent()).setText(dob);
            txt_email.setText(rs.getString(6));
            txt_mobile.setText(String.valueOf(rs.getLong(7)));
            txt_address.setText(rs.getString(8));
            txt_department.setText(rs.getString(9));
            txt_designation.setText(rs.getString(10));
            String joinDate = rs.getString(11);
            ((JTextField)DC_hiredate.getDateEditor().getUiComponent()).setText(joinDate);
            double sal = rs.getDouble(12);
            txt_salary.setText(String.valueOf(sal));
            txt_salary.setEditable(false);
            txt_jobtitle.setText(rs.getString(13));
            String sts = rs.getString(14);
            CB_status.setSelectedItem(sts);
            // image
            byte[] image = rs.getBytes(15);
            ImageIcon icon = new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(img.getWidth(), img.getHeight(), Image.SCALE_DEFAULT));
            img.setIcon(icon);
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, "No Record Found");
        }
    }
    
