    private void btn_addrecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addrecordActionPerformed
        // Add record to the database
        ImageIcon icon = new ImageIcon("src/images/addrecord.png");
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this record?", "Add Record", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, icon);
        if (option == JOptionPane.YES_OPTION)
        {
            try
            {
                String insertQuery = "insert into paydb.staff_info values (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement ps = conn.prepareStatement(insertQuery);
                ps.setString(1, txt_empid.getText());
                ps.setString(2, txt_firstname.getText());
                ps.setString(3, txt_surname.getText());
                ps.setString(4, gender);
                // date of birth
                String dob = ((JTextField)DC_dob.getDateEditor().getUiComponent()).getText();
                ps.setString(5, dob);
                ps.setString(6, txt_email.getText());
                ps.setLong(7, Long.valueOf(txt_mobile.getText()));
                ps.setString(8, txt_address.getText());
                ps.setString(9, txt_department.getText());
                ps.setString(10, txt_designation.getText());
                // date hired
                String dateHired = ((JTextField)DC_hiredate.getDateEditor().getUiComponent()).getText();
                ps.setString(11, dateHired);
                ps.setDouble(12, Double.valueOf(txt_salary.getText()));
                ps.setString(13, txt_jobtitle.getText());
                ps.setString(14, CB_status.getSelectedItem().toString());
                ps.setBytes(15, person_image);
                
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Record Saved Successfully");
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btn_addrecordActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // Updating the records
        ImageIcon icon = new ImageIcon("src/images/upc.png");
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to update this record?", "Add Record", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, icon);
        if (option == JOptionPane.YES_OPTION)
        {
            try
            {
                String updateQuery = "update paydb.staff_info set "
                + " firstname = ?, "
                + " lastname = ?, "
                + " email = ?, "
                + " contact = ?, "
                + " address = ?, "
                + " department = ?, "
                + " designation = ?, "
                + " jobtitle = ?, "
                + " status = ? where emp_id = ?";
                
                PreparedStatement ps = conn.prepareStatement(updateQuery);
                ps.setString(1, txt_firstname.getText());
                ps.setString(2, txt_surname.getText());
                ps.setString(3, txt_email.getText());
                ps.setLong(4, Long.valueOf(txt_mobile.getText()));
                ps.setString(5, txt_address.getText());
                ps.setString(6, txt_department.getText());
                ps.setString(7, txt_designation.getText());
                ps.setString(8, txt_jobtitle.getText());
                String ustatus = CB_status.getSelectedItem().toString();
                ps.setString(9, ustatus);
                ps.setString(10, txt_empid.getText());
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Record Updated");
                // clear the fields
                txt_empid.setEditable(true);
                txt_salary.setEditable(true);
                clear_contents();
                String id = generateID();
                txt_empid.setText(id);
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

