    // method for retrieving the data
    public void getData()
    {
       try
       {
            String selectQuery = "select emp_id, firstname, lastname, department, contact, salary, image from paydb.staff_info where emp_id = ?";
            PreparedStatement ps = conn.prepareStatement(selectQuery);
            ps.setString(1, txt_search.getText());
            ResultSet rs = ps.executeQuery();
            rs.first();
            
            lbl_empid.setText(rs.getString(1));
            lbl_firstname.setText(rs.getString(2));
            lbl_surname.setText(rs.getString(3));
            lbl_department.setText(rs.getString("department"));
            lbl_mobile.setText(String.valueOf(rs.getLong("contact")));
            lbl_salary.setText(String.valueOf(rs.getDouble("salary")));
            // image
            byte[] image = rs.getBytes("image");
            ImageIcon icon = new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(img.getWidth(), img.getHeight(), Image.SCALE_DEFAULT));
            img.setIcon(icon);
       }
       catch (SQLException e)
       {
           JOptionPane.showMessageDialog(null, "No Records Founds!");
       }
    }

