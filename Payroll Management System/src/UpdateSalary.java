    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // updating new salary
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?", "Select An Option", JOptionPane.YES_NO_OPTION);
        
        if (option == JOptionPane.YES_OPTION)
        {
            try
            {
                String updateQuery = "update paydb.staff_info set salary = ? where emp_id = ?";
                PreparedStatement ps = conn.prepareStatement(updateQuery);
                ps.setDouble(1,Double.valueOf(lbl_salary.getText()));
                ps.setString(2, lbl_empid.getText());
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Salary Updated!");
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

