    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Computing new salary
        double salary = Double.valueOf(lbl_salary.getText());
        double newSalary = 0;
        
        if (r_percent.isSelected() || r_amount.isSelected())
        {
            if (r_percent.isSelected())
            {
                double percent = Double.valueOf(txt_percent.getText());
                newSalary = ((salary * percent) / 100) + salary;
            }
            
            if (r_amount.isSelected())
            {
                double amount = Double.valueOf(txt_amount.getText());
                newSalary = amount + salary;
            }
            
            // rounding and displaying the new salary
            newSalary = Math.round(newSalary);
            lbl_salary.setText(String.valueOf(newSalary));
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please Select Percentage Or Amount Radio Button!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

