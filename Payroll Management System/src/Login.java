    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        ImageIcon icon = new ImageIcon("src/images/forgetpass.png");
        String id = (String)JOptionPane.showInputDialog(null, "Enter your ID: ", "Forget Password", JOptionPane.INFORMATION_MESSAGE, icon, null, null);
        
        try
        {
            String selectQuery = "select username, password from paydb.newaccount where id = ?";
            PreparedStatement sps = conn.prepareStatement(selectQuery);
            sps.setString(1, id);
            ResultSet srs = sps.executeQuery();
            srs.next();
            String user = srs.getString("username");
            String pass = srs.getString("password");
            JOptionPane.showMessageDialog(null, ("Username : " + user + "\nPassword : " + pass));
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
       try
       {
           String selectQuery = "select username, password from paydb.newaccount";
           PreparedStatement pst = conn.prepareStatement(selectQuery);
           ResultSet rs = pst.executeQuery();
           
           ArrayList<String> ulist = new ArrayList();
           ArrayList<String> plist = new ArrayList();
           
           while (rs.next())
           {
               String user = rs.getString("username");
               String pass = rs.getString("password");
               
               ulist.add(user);
               plist.add(pass);
           }
           
           // validation
           if (ulist.contains(txt_username.getText()) && plist.contains(txt_pass.getText()))
           {
               try
               {
                   String selectQuery2 = "select id, firstname, surname from paydb.newaccount where username = ?";
                   PreparedStatement spst = conn.prepareStatement(selectQuery2);
                   spst.setString(1, txt_username.getText());
                   ResultSet srs = spst.executeQuery();
                   srs.first();
                   
                   String aid = srs.getString(1);
                   Admin.id = aid;
                   String fn = srs.getString(2);
                   Admin.firstname = fn;
                   String sn = srs.getString(3);
                   Admin.lastname = sn;
               }
               catch (SQLException e)
               {
                   JOptionPane.showMessageDialog(null, e);
               }
               
               // navigating the main menu
               this.dispose();
               JOptionPane.showMessageDialog(null, "Login Success!");
               new MainMenu().setVisible(true);
           }
           else
           {
               JOptionPane.showMessageDialog(null, "Username and Password you entered are wrong!","Error", JOptionPane.ERROR_MESSAGE);
           }
       }
       catch (SQLException e)
       {
           JOptionPane.showMessageDialog(null, e);
       }
    }//GEN-LAST:event_btn_loginActionPerformed

