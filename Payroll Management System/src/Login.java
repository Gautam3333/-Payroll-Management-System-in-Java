    // current date and time 
    public void current_date()
    {   
        Thread thread = new Thread(){
            @Override
            public void run() {
                try {
                    while (true) {
                    GregorianCalendar gc = new GregorianCalendar();
                    int hour = gc.get(Calendar.HOUR);
                    int minute = gc.get(Calendar.MINUTE);
                    int second = gc.get(Calendar.SECOND);
                    int meridiem = gc.get(Calendar.AM_PM);
                    
                    // date
                    int day = gc.get(Calendar.DAY_OF_MONTH);
                    int month = gc.get(Calendar.MONTH);
                    int year = gc.get(Calendar.YEAR);
                    
                    // display time and date
                    if (minute < 10) {
                        lbl_time.setText(hour + ":" + "0" + minute + ":" + second + " " + (meridiem == 0 ? "AM" : "PM"));
                    }
                    else {
                        lbl_time.setText(hour + ":" + minute + ":" + second + " " + (meridiem == 0 ? "AM" : "PM"));
                    }
                    
                    lbl_date.setText(day + "-" + monthName(month) + "-" + year);
                    
                    sleep(1000);
                    
                }
                }
                catch (InterruptedException e) {
                }
            }
        };
        
        thread.start();
    }
    
    public String monthName(int num)
    {
        String[] month = new String[]{"Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"};
        return month[num];
    }

