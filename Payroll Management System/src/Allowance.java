    // method for retrieving data
    public void getRecord()
    {
        try
        {
            String selectQuery = "select emp_id, firstname, lastname, department, contact, salary, image from paydb.staff_info where emp_id = ?";
            PreparedStatement pst = conn.prepareStatement(selectQuery);
            pst.setString(1, txt_search4.getText());
            ResultSet rs = pst.executeQuery();
            rs.first();
            
            lbl_empid.setText(rs.getString("emp_id"));
            lbl_firstname.setText(rs.getString("firstname"));
            lbl_surname.setText(rs.getString("lastname"));
            lbl_department.setText(rs.getString("department"));
            lbl_mobile.setText(String.valueOf(rs.getLong("contact")));
            lbl_salary.setText(String.valueOf(rs.getDouble("salary")));
            byte[] picture = rs.getBytes("image");
            ImageIcon icon = new ImageIcon(new ImageIcon(picture).getImage().getScaledInstance(img4.getWidth(), img4.getHeight(), Image.SCALE_DEFAULT));
            img4.setIcon(icon);
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, "No Records Found!");
        }
    }
    
