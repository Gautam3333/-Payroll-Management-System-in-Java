    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // saving the record
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to save? ", "Select An Option", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION)
        {
            try
            {
                String insertQuery = "insert into paydb.allowance values (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement ps = conn.prepareStatement(insertQuery);
                ps.setString(1, lbl_empid.getText());
                ps.setString(2, lbl_firstname.getText());
                ps.setString(3, lbl_surname.getText());
                ps.setString(4, lbl_department.getText());
                ps.setLong(5, Long.valueOf(lbl_mobile.getText()));
                ps.setDouble(6, Double.valueOf(lbl_salary.getText()));
                ps.setDouble(7, Double.valueOf(txt_oAmount.getText()));
                ps.setDouble(8, Double.valueOf(txt_medical.getText()));
                ps.setDouble(9, Double.valueOf(txt_transport.getText()));
                ps.setDouble(10, Double.valueOf(txt_other.getText()));
                ps.setDouble(11, Double.valueOf(lbl_allowance.getText()));
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Record Saved Successfully!");
                clearpanel();
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null, "Record Already Saved");
                clearpanel();
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // deleting the records
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Select An Option", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_NO_OPTION)
        {
            try
            {
                String deleteQuery = "delete from paydb.allowance where emp_id = ?";
                PreparedStatement ps = conn.prepareStatement(deleteQuery);
                ps.setString(1, lbl_empid.getText());
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Record Deleted Successfully");
                clearpanel();
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calculateActionPerformed
        // calculating the allowance
        GregorianCalendar gc = new GregorianCalendar();
        int monthDays = gc.getActualMaximum(Calendar.DAY_OF_MONTH);
        double salary = Double.valueOf(lbl_salary.getText());
        int times = Integer.valueOf(txt_times.getText());
        double salaryOneDay = salary / monthDays;
        double overtimeAmount = (salaryOneDay * times) / 2;
        overtimeAmount = Math.round(overtimeAmount);
        txt_oAmount.setText(String.valueOf(overtimeAmount));
        
        double medical = Double.valueOf(txt_medical.getText());
        double transport = Double.valueOf(txt_transport.getText());
        double other = Double.valueOf(txt_other.getText());
        
        double totalAmount = (overtimeAmount + medical + transport + other);
        lbl_allowance.setText(String.valueOf(totalAmount));
    }//GEN-LAST:event_btn_calculateActionPerformed

